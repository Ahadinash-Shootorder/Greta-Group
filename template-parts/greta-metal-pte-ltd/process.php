            <h2 class="industry-page-process-title">Basic Rice Milling Processes</h2>
            <div class="industry-page-process-title-underline"></div>
        </div>

        <div class="industry-page-process-content">
            <p class="industry-page-process-paragraph">
                Paddy in its raw form cannot be consumed by human beings. It needs to be suitably processed for obtaining rice. Rice milling is the process which helps in removal of hulls and barns from paddy grains to produce polished rice. Rice forms the basic primary processed product obtained from paddy and this is further processed for obtaining various secondary and tertiary products.
            </p>

            <p class="industry-page-process-subheading">The basic rice milling processes consist of:</p>

            <p class="industry-page-process-paragraph">
                The rice milling process begins with the incoming inspection of raw paddy, ensuring only high-quality grain enters the system. This is followed by pre-cleaning, which removes visible impurities and debris. The cleaned paddy is then transferred to a storage silo for holding before further processing.
            </p>

            <p class="industry-page-process-paragraph">
                The paddy undergoes primary steaming, then moves to soaking in hot water, followed by secondary steaming. These thermal treatments help gelatinize starch and improve the nutritional profile of the grain. After steaming, the paddy is dried at a regulated temperature, then stored in silos to stabilize moisture content.
            </p>

            <p class="industry-page-process-paragraph">
                The dried paddy then goes through cleaning and de-stoning, which remove residual foreign particles and heavier contaminants like stones. The cleaned grain enters the de-husking phase, where the outer husk is mechanically removed. This is followed by husk separation, and the husk is sent to a designated husk yard.
            </p>
